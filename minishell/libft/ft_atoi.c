#include "libft.h"

long long	ft_atoi(const char *str)
{
	long long		res;
	int				minus;

	res = 0;
	minus = 1;
	if (*str == '-')
	{
		minus = -minus;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return ((long long)(minus * res));
}
